    private void tbEventosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEventosMouseClicked
        if(tbEventos.getSelectedRow() != -1){
            txtNome.setText((String) tableModel.getValueAt(tbEventos.getSelectedRow(), 0));
            txtData.setText((String) tableModel.getValueAt(tbEventos.getSelectedRow(), 1));
            txtDuracao.setText((String) tableModel.getValueAt(tbEventos.getSelectedRow(), 2));
            txtResponsavel.setText((String) tableModel.getValueAt(tbEventos.getSelectedRow(), 3));
            txtCusto.setText((String) tableModel.getValueAt(tbEventos.getSelectedRow(), 4));
            txtObjetivo.setText((String) tableModel.getValueAt(tbEventos.getSelectedRow(), 5));
            txtMeta.setText((String) tableModel.getValueAt(tbEventos.getSelectedRow(), 6));
            if(tableModel.getValueAt(tbEventos.getSelectedRow(), 7).equals("Ação")){
                boxTipo.setSelectedIndex(1);
            }else if(tableModel.getValueAt(tbEventos.getSelectedRow(), 7).equals("Campanha")){
                boxTipo.setSelectedIndex(2);
            }
        }
    }//GEN-LAST:event_tbEventosMouseClicked

    private void btImportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btImportarActionPerformed
        try{
            eventoImport = leitorArquivo.importarArquivo("src\\main\\java\\arquivos\\EventoImport.txt");
            for (int i=0;i<eventoImport.size();i++){
            tableModel.addRow(eventoImport.get(i));
            }
            JOptionPane.showMessageDialog(null, "Eventos importados com Sucesso!!");
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, "Erro durante a importação de eventos");
            e.printStackTrace();
        }
        
        
    }//GEN-LAST:event_btImportarActionPerformed

