    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DoacaoTableModel listaDoacao = new DoacaoTableModel();
        EventoTableModel listaEvento = new EventoTableModel();
        ArrayList<Doacao> doacoes;
        ArrayList<Evento> eventos;
        doacoes = listaDoacao.getAll();
        eventos = listaEvento.getAll();
        Document document = new Document();
        float custo;
        float saldoEvento = 0;
        float saldoDoacao = 0;
        
        
        try{
            PdfWriter.getInstance(document, new FileOutputStream("Relatório financeiro.pdf"));
            
            
            document.open();
            Paragraph titulo = new Paragraph("MORAIS VOLUNTARIADO\nRelatório Financeiro");
            titulo.setAlignment(Element.ALIGN_CENTER);
            document.add(titulo);
            document.add(new Paragraph("\n\nEVENTOS"));
            for (int i = 0; i< eventos.size(); i++){
                custo = 0;
                document.add(new Paragraph("\n###############################"));
                document.add(new Paragraph("\nEvento: "+eventos.get(i).getNome()));
                document.add(new Paragraph("Pessoa Responsável pelo Evento: " + eventos.get(i).getResponsavel()));
                document.add(new Paragraph("Custo total do Evento: " + eventos.get(i).getCusto()));
                document.add(new Paragraph("Orçamento recebido do Evento: " + eventos.get(i).getMeta()));
                custo -= Float.parseFloat(eventos.get(i).getCusto());
                custo += Float.parseFloat(eventos.get(i).getMeta());
                if(custo>=0){
                    document.add(new Paragraph("Saldo total do Evento: R$ " + custo));
                }else{
                    document.add(new Paragraph("Saldo total do Evento: R$ -" + custo));
                }
                saldoEvento += custo;
                document.add(new Paragraph("\n###############################"));
            }
            document.add(new Paragraph("\n\nDOAÇÕES"));
            for (int i = 0; i< eventos.size(); i++){
                document.add(new Paragraph("\n###############################"));
                document.add(new Paragraph("Doação feita por: " + doacoes.get(i).getDoador()));
                document.add(new Paragraph("Valor doado: " + doacoes.get(i).getQuantia()));
                saldoDoacao += Float.parseFloat(doacoes.get(i).getQuantia());
            }
            
            document.add(new Paragraph("\n\nSALDO TOTAL"));
            if(saldoEvento>=0){
                document.add(new Paragraph("Balanço relacionado a eventos: R$ "+saldoEvento));
            }else{
                document.add(new Paragraph("Balanço relacionado a eventos: R$ -"+saldoEvento));
            }
            document.add(new Paragraph("Balanço relacionado a doações: R$ "+saldoDoacao));
            document.add(new Paragraph("Balanço final: R$ "+(saldoDoacao+saldoEvento)));
        }catch(FileNotFoundException | DocumentException ex) {
            System.out.println("Error"+ex);
        }finally{
            document.close();
        }
        
        try{
            Desktop.getDesktop().open(new File("Relatório Financeiro.pdf"));
            
        }catch (Exception e){
            System.out.println("Error:"+ e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

