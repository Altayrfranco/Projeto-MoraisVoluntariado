    public GerenciaFinanceira() {
        initComponents();
        this.setLocationRelativeTo(null);
        DoacaoTableModel listaDoacao = new DoacaoTableModel();
        EventoTableModel listaEvento = new EventoTableModel();
        ArrayList<Doacao> doacoes;
        ArrayList<Evento> eventos;
        doacoes = listaDoacao.getAll();
        eventos = listaEvento.getAll();
        Float receitaDoacao = 0f;
        Float receitaEvento = 0f;
        Float despesaEvento = 0f;
        Float receitaTotal = 0f;
        String[] receitaDoacoes = new String[doacoes.size()];
        String[] receitaEventos = new String[eventos.size()];
        String[] despesaEventos = new String[eventos.size()];
        String[] receitaCompleta = new String[3];
        
        for(int i=0;i<receitaDoacoes.length;i++){
            if(doacoes.get(i).getMaterial().equals("Dinheiro")){
                receitaDoacoes[i] = doacoes.get(i).getDoador() + ": R$ " + doacoes.get(i).getQuantia();
                receitaDoacao += Float.parseFloat(doacoes.get(i).getQuantia());
            }
        }
        for(int i=0;i<receitaEventos.length;i++){
            receitaEventos[i] = eventos.get(i).getNome() + ": R$ " + eventos.get(i).getMeta();
            receitaEvento += Float.parseFloat(eventos.get(i).getMeta());
        }
        for(int i=0;i<despesaEventos.length;i++){
            despesaEventos[i] = eventos.get(i).getNome() + ": - R$ " + eventos.get(i).getCusto();
            despesaEvento -= Float.parseFloat(eventos.get(i).getCusto());
        }
        
        doacaoReceita.setListData(receitaDoacoes);
        eventoReceita.setListData(receitaEventos);
        eventoDespesa.setListData(despesaEventos);
        txtReceitaDoacao.setText("R$ " +receitaDoacao);
        txtReceitaEvento.setText("R$ " +receitaEvento);
        txtDespesaEvento.setText("R$ " +despesaEvento);
        
        receitaCompleta[0] = ("Receita Doações: R$ " + receitaDoacao);
        receitaCompleta[1] = ("Receita Eventos: R$ " + receitaEvento);
        receitaCompleta[2] = ("Receita Eventos: R$ -" + despesaEvento);
        
        receitaTotal = receitaDoacao+receitaEvento+despesaEvento;
        
        financeiroCompleto.setListData(receitaCompleta);
        
        if(receitaTotal >= 0){
            totalReceita.setText("R$ " + receitaTotal);
        }else if(receitaTotal < 0){
            totalReceita.setText("R$ -"+receitaTotal);
        }
        
    }
