    private void jTDoacaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTDoacaoMouseClicked
        if(jTDoacao.getSelectedRow() != -1){
            jTxtDoador.setText((String) tableModel.getValueAt(jTDoacao.getSelectedRow(), 0));
            jTxtMaterialDoacao.setText((String) tableModel.getValueAt(jTDoacao.getSelectedRow(), 1));
            jTxtQuantia.setText((String) tableModel.getValueAt(jTDoacao.getSelectedRow(), 2));
            jTxtDataDoacao.setText((String) tableModel.getValueAt(jTDoacao.getSelectedRow(), 3));
            if(tableModel.getValueAt(jTDoacao.getSelectedRow(), 4).equals("Local")){
                jBoxMetodoEntrega.setSelectedIndex(1);
            }else if(tableModel.getValueAt(jTDoacao.getSelectedRow(), 4).equals("Coleta")){
                jBoxMetodoEntrega.setSelectedIndex(2);
            }
            if(tableModel.getValueAt(jTDoacao.getSelectedRow(), 5).equals("Sim")){
                jRecorrente.setSelected(true);
            }else jRecorrente.setSelected(false);

        }
    }//GEN-LAST:event_jTDoacaoMouseClicked

