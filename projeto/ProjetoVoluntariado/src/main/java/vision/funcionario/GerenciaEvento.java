    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        String nome = txtNome.getText();
        String data = txtData.getText();
        String duracao = txtDuracao.getText();
        String responsavel = txtResponsavel.getText();
        String custo = txtCusto.getText();
        String objetivo = txtObjetivo.getText();
        String meta = txtMeta.getText();
        String tipo = (String) boxTipo.getSelectedItem();
        
        Evento e = new Evento(nome, data, duracao, responsavel, custo, objetivo, meta, tipo);
        
        tableModel.addRow(e);
        
        JOptionPane.showMessageDialog(null, "Evento registrada com sucesso!");
        
        txtNome.setText("");
        txtData.setText("");
        txtDuracao.setText("");
        txtResponsavel.setText("");
        txtCusto.setText("");
        txtObjetivo.setText("");
        txtMeta.setText("");
        boxTipo.setSelectedIndex(0);

    }//GEN-LAST:event_btSalvarActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        if(tbEventos.getSelectedRow() != -1){
            switch(JOptionPane.showConfirmDialog(null, "Confirma a alteração de dados?")){
                case SIM:
                tableModel.setValueAt(txtNome.getText(), tbEventos.getSelectedRow(), 0);  
                tableModel.setValueAt(txtData.getText(), tbEventos.getSelectedRow(), 1);  
                tableModel.setValueAt(txtDuracao.getText(), tbEventos.getSelectedRow(), 2);  
                tableModel.setValueAt(txtResponsavel.getText(), tbEventos.getSelectedRow(), 3);  
                tableModel.setValueAt(Float.parseFloat(txtCusto.getText()), tbEventos.getSelectedRow(), 4);  
                tableModel.setValueAt(txtObjetivo.getText(), tbEventos.getSelectedRow(), 5);  
                tableModel.setValueAt(txtMeta.getText(), tbEventos.getSelectedRow(), 6);  
                tableModel.setValueAt(boxTipo.getSelectedItem(), tbEventos.getSelectedRow(), 7);  
                JOptionPane.showMessageDialog(null, "Evento alterado com sucesso!");
                break;
            case NAO:;
            case CANCELAR:
                break;
            }
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        if(tbEventos.getSelectedRow() != -1){
            switch(JOptionPane.showConfirmDialog(null, "Confirma a exclusão do Evento?")){
                case SIM:
                    tableModel.removeRow(tbEventos.getSelectedRow());
                    JOptionPane.showMessageDialog(null, "Evento deletado com sucesso!");
                    break;
                case NAO:
                case CANCELAR:
                    break;
            }
        }
    }//GEN-LAST:event_btRemoverActionPerformed

