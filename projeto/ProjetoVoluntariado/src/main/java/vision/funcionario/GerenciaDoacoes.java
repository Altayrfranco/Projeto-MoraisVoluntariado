    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        String doador = jTxtDoador.getText();
        String dataDoacao = jTxtDataDoacao.getText();
        String materialDoacao = jTxtMaterialDoacao.getText();
        Float quantia = Float.parseFloat(jTxtQuantia.getText());
        String metodoEntrega = (String) jBoxMetodoEntrega.getSelectedItem();
        boolean recorrente = jRecorrente.isSelected();
        
        Doacao d = new Doacao(doador, dataDoacao, materialDoacao, quantia, metodoEntrega, recorrente);
        
        tableModel.addRow(d);
        
        JOptionPane.showMessageDialog(null, "Doação registrada com sucesso!");
        jTxtDoador.setText("");
        jTxtDataDoacao.setText("");
        jTxtMaterialDoacao.setText("");
        jTxtQuantia.setText("");
        jBoxMetodoEntrega.setSelectedIndex(0);
        jRecorrente.setSelected(false);
        
        
    }//GEN-LAST:event_jBtSalvarActionPerformed

    private void jBtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtExcluirActionPerformed
        if(jTDoacao.getSelectedRow() != -1){
            switch(JOptionPane.showConfirmDialog(null, "Confirma a exclusão da doação?")){
                case SIM:
                    tableModel.removeRow(jTDoacao.getSelectedRow());
                    JOptionPane.showMessageDialog(null, "Doação deletada com sucesso!");
                    break;
                case NAO:
                case CANCELAR:
                    break;
            }
        }
        
    }//GEN-LAST:event_jBtExcluirActionPerformed

    private void jBtAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtAlterarActionPerformed
        if(jTDoacao.getSelectedRow() != -1){
            switch(JOptionPane.showConfirmDialog(null, "Confirma a alteração de dados?")){
                case SIM:
                tableModel.setValueAt(jTxtDoador.getText(), jTDoacao.getSelectedRow(), 0);  
                tableModel.setValueAt(jTxtMaterialDoacao.getText(), jTDoacao.getSelectedRow(), 1);
                tableModel.setValueAt(Float.parseFloat(jTxtQuantia.getText()), jTDoacao.getSelectedRow(), 2);
                tableModel.setValueAt(jTxtDataDoacao.getText(), jTDoacao.getSelectedRow(), 3);
                tableModel.setValueAt(jBoxMetodoEntrega.getSelectedItem(), jTDoacao.getSelectedRow(), 4);
                tableModel.setValueAt(jRecorrente.isSelected(), jTDoacao.getSelectedRow(), 5);
                JOptionPane.showMessageDialog(null, "Evento alterado com sucesso!");
                break;
            case NAO:;
            case CANCELAR:
                break;
            }
        }
    }//GEN-LAST:event_jBtAlterarActionPerformed

